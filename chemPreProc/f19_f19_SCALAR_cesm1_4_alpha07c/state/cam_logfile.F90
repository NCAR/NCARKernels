
module cam_logfile

!----------------------------------------------------------------------- 
! 
! Purpose: This module is responsible for managing the logical unit 
!          of CAM's output log
! 
! Author: mvr, Sep 2007
! 
!-----------------------------------------------------------------------

!-----------------------------------------------------------------------
!- use statements ------------------------------------------------------
!-----------------------------------------------------------------------
!-----------------------------------------------------------------------
!- module boilerplate --------------------------------------------------
!-----------------------------------------------------------------------
   implicit none

!-----------------------------------------------------------------------
! Public interfaces ----------------------------------------------------
!-----------------------------------------------------------------------
!-----------------------------------------------------------------------
! Public data ----------------------------------------------------------
!-----------------------------------------------------------------------
   integer :: iulog = 6

!-----------------------------------------------------------------------
! Private data ---------------------------------------------------------
!-----------------------------------------------------------------------
!-----------------------------------------------------------------------
! Subroutines and functions --------------------------------------------
!-----------------------------------------------------------------------

   PUBLIC kw_externs_in_cam_logfile
   
   CONTAINS
   
   !write in state subroutine for kw_externs_in_cam_logfile
   SUBROUTINE kw_externs_in_cam_logfile(kgen_unit)
       INTEGER, INTENT(IN) :: kgen_unit
       LOGICAL :: kgen_istrue
       REAL(KIND=8) :: kgen_array_sum
       
       kgen_istrue = .TRUE.
       WRITE (UNIT = kgen_unit) iulog
   END SUBROUTINE kw_externs_in_cam_logfile
   
end module cam_logfile
